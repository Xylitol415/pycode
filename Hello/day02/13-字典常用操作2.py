student = {'name': '张三丰', 'age': 18, 'sex': '男', 'number': '123'}
# 1. len()
print("len:", len(student))

# 2.keys() 获取所有元素的key值
print("keys:", student.keys())

# 3.values() 获取所有元素value值
print("values:", student.values())

# 4.items  获取所有元素的key，value值
print("items:", student.items())

